def my_func(x, y):
    """
    Формула возведения в отрицательную степень x^-y = 1 / (x ^ y) (^ - Символ степени)
    :param x: Основание степени
    :param y: Показатель степени
    :param z: Переменная для нахождения степени x
    """
    z = 1
    for i in range(abs(y)):
        z *= x
    if y >= 0:
        return x
    else:
        return 1 / z


