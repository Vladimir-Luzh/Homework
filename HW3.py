# 5. Программа запрашивает у пользователя строку чисел, разделенных пробелом. При нажатии Enter должна выводиться
# сумма чисел. Пользователь может продолжить ввод чисел, разделенных пробелом и снова нажать Enter. Сумма вновь
# введенных чисел будет добавляться к уже подсчитанной сумме. Но если вместо числа вводится специальный
# символ, выполнение программы завершается. Если специальный символ введен после нескольких чисел, то
# вначале нужно добавить сумму этих чисел к полученной ранее сумме и после этого завершить программу.


def calc_func():
    total = 0
    stop = False
    while stop == False:
        numbers = input("Введите числа или введите q для выхода").split()

        current_sum = 0
        for el in range(len(numbers)):
            if numbers[el] == 'q':
                stop = True
                break
            else:
                current_sum += int(numbers[el])
        total = total + current_sum
        print(f'Текущая сумма {total}')
    print(f'Итоговая сумма {total}')


calc_func()


